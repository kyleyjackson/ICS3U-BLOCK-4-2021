public class SquareRoot {
    public static void main(String[] args) {
        double a = 10;
        double b = 20;
        double c = 30;
        double x = 40;
        double equation = ((a * Math.sqrt(x)) * (a * Math.sqrt(x)) + b * Math.sqrt(x) + c);
        System.out.println(equation);
    }
}
