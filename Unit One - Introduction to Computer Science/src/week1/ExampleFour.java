package week1;

/**
 * Variables - Used to store information/data/value Primitive Data Types => int,
 * double, boolean, char Assignment / Assignment Operators - Assigns a value to
 * a variable (=)
 */
public class ExampleFour {
    public static void main(String[] args) {
        int markOne = 75, markTwo = 80, markThree = 87; // Delacred and assigned in the same line (can do them apart
                                                        // later)

        int average = (markOne + markTwo + markThree) / 3;

        System.out.println("The average is: " + average);
    }
}
